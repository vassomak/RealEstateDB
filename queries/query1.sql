# Έστω ότι το ακίνητο 578 πουλήθηκε και χρειαζόμαστε τα στοιχεία των πελατών που είχαν
# προγραμματίσει επίσκεψη σε αυτό, για να τους ενημερώσουμε ότι το ραντεβού τους ακυρώνεται.
SELECT πελάτης.ΠελάτηςID, ονοματεπώνυμο, τηλέφωνο, ημερομηνία, ώρα
FROM realestatedb.πελάτης JOIN realestatedb.πελάτης_επισκέπτεται_ακίνητο on πελάτης.ΠελάτηςID = πελάτης_επισκέπτεται_ακίνητο.ΠελάτηςID
WHERE πελάτης_επισκέπτεται_ακίνητο.ΑκίνητοID = '578' 