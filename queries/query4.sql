# Για μία συγκεκριμένη περιοχή (έστω Κάτω Τούμπα Θεσσαλονίκης) 
# θέλουμε να εμφανίσουμε όλα τα διαθέσιμα διαμερίσματα (το ID τους) προς ενοικίαση που απαιτούν ποσό < 500 ευρώ.
SELECT ΑκίνητοID
FROM  ακίνητο 
WHERE είδος_διάθεσης = 'ενοικίαση' AND ποσό < 500 AND περιοχή = 'Κάτω τούμπα' AND πόλη='Θεσσαλονίκη'
