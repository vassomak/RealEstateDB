#Ερώτημα το οποίο βρίσκει τους πελάτες, τις ημερομηνίες υπογραφής των συμβολαίων, τη διάρκεια τους 
# και τον ιδιοκτήτη για το ακίνητο με αριθμό 21
SELECT ιδιοκτήτης.ονοματεπώνυμο AS Ιδιοκτήτης, συμβόλαιο.ΑκίνητοID AS αριθμός_ακινήτου, πελάτης.ονοματεπώνυμο AS Πελάτης, συμβόλαιο.ημερομηνία_υπογραφής, συμβόλαιο.διάρκεια
FROM   συμβόλαιο JOIN ιδιοκτήτης ON συμβόλαιο.ΙδιοκτήτηςID = ιδιοκτήτης.ΙδιοκτήτηςID 
       JOIN πελάτης ON συμβόλαιο.ΠελάτηςID = πελάτης.ΠελάτηςID
WHERE  ΑκίνητοID = 21